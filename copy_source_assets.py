# ── Secret redaction (mirrors regen_secrets.py placeholder style) ───────────

# A key is secret-looking when its LAST underscore-segment(s) match one of
# these suffixes. Deliberately does NOT match max_tokens / request_timeout /
# device_name / chat history fields.
_SECRET_KEY_RE = re.compile(
    r"(?i)^(?:.*[_\-.])?("
    r"api_key|apikey|api_hash|api_id|token|bot_token|secret|client_secret|"
    r"password|passwd|access_key|chat_id|listen_chat|session_string|username"
    r")$"
)

# YAML line:   <indent><key>: <value>     (comments after the value survive)
_YAML_KV_RE = re.compile(r"^(\s*)([A-Za-z0-9_\-.]+)(\s*:\s*)(.*?)(\s*(#.*)?)$")

_SKIP_VALUES = {"", "''", '""', "[]", "{}", "null", "~", "None", "0"}


def _placeholder(key: str) -> str:
    """regen_secrets.py-compatible placeholder."""
    return f"<{key} goes here>"


def _is_secret_key(key: str) -> bool:
    return bool(_SECRET_KEY_RE.match(key.strip()))


def _value_needs_redaction(value: str) -> bool:
    v = value.strip().strip("'\"")
    if v in _SKIP_VALUES or value.strip() in _SKIP_VALUES:
        return False
    if v.startswith("<") and v.endswith("goes here>"):
        return False  # already a placeholder
    return True


def _redact_json_obj(obj: Any, hits: List[str]) -> Any:
    """Deep-walk a parsed JSON structure replacing secret-looking values."""
    if isinstance(obj, dict):
        out = {}
        for k, v in obj.items():
            if (_is_secret_key(str(k)) and isinstance(v, (str, int, float))
                    and _value_needs_redaction(str(v))):
                out[k] = _placeholder(str(k))
                hits.append(str(k))
            else:
                out[k] = _redact_json_obj(v, hits)
        return out
    if isinstance(obj, list):
        return [_redact_json_obj(item, hits) for item in obj]
    return obj


def _redact_json_text(text: str) -> tuple[str, int]:
    """Redact a JSON document; returns (new_text, redaction_count).
    Fail-open: unparseable JSON is returned verbatim with 0 redactions."""
    try:
        data = json.loads(text)
    except Exception:
        return text, 0
    hits: List[str] = []
    redacted = _redact_json_obj(data, hits)
    if not hits:
        return text, 0
    return json.dumps(redacted, indent=2, ensure_ascii=False) + "\n", len(hits)


def _redact_yaml_text(text: str) -> tuple[str, int]:
    """Line-based YAML redaction (comments and formatting survive intact)."""
    out_lines: List[str] = []
    count = 0
    for line in text.splitlines(keepends=False):
        m = _YAML_KV_RE.match(line)
        if m:
            indent, key, sep, value, trail = m.group(1), m.group(2), m.group(3), m.group(4), m.group(5) or ""
            if _is_secret_key(key) and _value_needs_redaction(value):
                out_lines.append(f"{indent}{key}{sep}'{_placeholder(key)}'{trail}")
                count += 1
                continue
        out_lines.append(line)
    new_text = "\n".join(out_lines)
    if text.endswith("\n"):
        new_text += "\n"
    return new_text, count


# The self-modify snapshot must never carry a user's REAL contacts (phone numbers
# / @handles are PII the key-based redactor above does NOT scrub). So contacts.json
# is replaced wholesale with this safe example in the snapshot.
_CONTACTS_SNAPSHOT_PLACEHOLDER = (
    '{\n'
    '  "_README": "Tlamatini Contacts book. Add people here, then say \'send a '
    "WhatsApp / Telegram to <name>'. Fields: name, aliases, telegram (@username "
    'for people), whatsapp (+phone), email.",\n'
    '  "contacts": [\n'
    '    {"name": "Example Person", "aliases": ["Example"], '
    '"telegram": "@example", "whatsapp": "+10000000000", "email": "example@example.com"}\n'
    '  ]\n'
    '}\n'
)


def _wants_redaction(rel_posix: str) -> str:
    """Return 'json' / 'yaml' / 'contacts' / '' for files whose secrets must be scrubbed."""
    if rel_posix == "Tlamatini/agent/config.json":
        return "json"
    if rel_posix == "Tlamatini/agent/external_mcps.json":
        return "json"  # External MCP catalog — a server's env block may hold keys
    if rel_posix == "Tlamatini/agent/contacts.json":
        return "contacts"  # PII (phones / handles) — replace with a safe example
    if (rel_posix.startswith("Tlamatini/agent/agents/")
            and rel_posix.endswith("config.yaml")):
        return "yaml"
    return ""


# ── Walk + copy ──────────────────────────────────────────────────────────────

def _skip_file(name: str) -> bool:
    if name in EXCLUDED_FILE_NAMES:
        return True
    if Path(name).suffix.lower() in EXCLUDED_EXTENSIONS:
        return True
    return any(fnmatch.fnmatch(name, pat) for pat in EXCLUDED_FILE_GLOBS)


def _force_remove_readonly(func, path, _exc_info):
    """rmtree onerror hook: some caches (notably Go's module cache) write files
    READ-ONLY on Windows, so clearing a stale snapshot dest can raise WinError 5.
    Clear the read-only bit and retry so a rebuild is never blocked by leftovers."""
    try:
        os.chmod(path, 0o700)
        func(path)
    except Exception:
        pass


def copy_source_assets(repo_root: Path | str = REPO_ROOT,
                       dest: Path | str = DEFAULT_DEST,
                       redact: bool = True) -> Dict[str, Any]:
    """Build the self-modify source snapshot. Returns a stats dict."""
    started = time.time()
    repo_root = Path(repo_root).resolve()
    dest = Path(dest).resolve()
    if dest == repo_root:
        raise ValueError("dest must not be the repo root itself")
    if dest.exists():
        shutil.rmtree(dest, onerror=_force_remove_readonly)
    dest.mkdir(parents=True, exist_ok=True)

    files_copied = 0
    files_skipped = 0
    bytes_copied = 0
    redacted_files: Dict[str, int] = {}
    errors: List[str] = []

    for current_dir, dir_names, file_names in os.walk(repo_root):
        cur = Path(current_dir)
        # prune excluded directories in place (and never descend into dest)
        dir_names[:] = sorted(
            d for d in dir_names
            if d not in EXCLUDED_DIR_NAMES and (cur / d).resolve() != dest
        )
        for fname in sorted(file_names):
            src = cur / fname
            rel = src.relative_to(repo_root)
            rel_posix = rel.as_posix()
            if _skip_file(fname):
                files_skipped += 1
                continue
            dst = dest / rel
            try:
                dst.parent.mkdir(parents=True, exist_ok=True)
                mode = _wants_redaction(rel_posix) if redact else ""
                if mode:
                    text = src.read_text(encoding="utf-8", errors="replace")
                    if mode == "json":
                        new_text, n = _redact_json_text(text)
                    elif mode == "contacts":
                        new_text, n = _CONTACTS_SNAPSHOT_PLACEHOLDER, 1
                    else:
                        new_text, n = _redact_yaml_text(text)
                    dst.write_text(new_text, encoding="utf-8")
                    if n:
                        redacted_files[rel_posix] = n
                else:
                    shutil.copy2(src, dst)
                files_copied += 1
                bytes_copied += dst.stat().st_size
            except OSError as exc:
                errors.append(f"{rel_posix}: {exc}")

    if files_copied == 0:
        raise RuntimeError(f"copy_source_assets produced an EMPTY snapshot at {dest} "
                           f"from {repo_root} — refusing to continue.")

    # Completeness guarantee — fail loudly if a build-/run-critical source was
    # dropped (e.g. by a future exclusion rule) rather than ship a broken tree.
    missing = [rel for rel in REQUIRED_SNAPSHOT_FILES if not (dest / rel).is_file()]
    if missing:
        raise RuntimeError(
            "copy_source_assets snapshot is INCOMPLETE — these required source "
            "files are missing from " + str(dest) + ":\n  - " + "\n  - ".join(missing)
            + "\n(Either the file was removed from the repo, or an exclusion rule "
            "wrongly dropped it — fix before shipping a self-modify build.)"
        )

    stats: Dict[str, Any] = {
        "generated_at": datetime.now().isoformat(timespec="seconds"),
        "repo_root": str(repo_root),
        "dest": str(dest),
        "files_copied": files_copied,
        "files_skipped": files_skipped,
        "bytes_copied": bytes_copied,
        "megabytes_copied": round(bytes_copied / 1048576, 2),
        "files_redacted": len(redacted_files),
        "redactions": redacted_files,
        "restore_from_install": RESTORE_FROM_INSTALL,
        "errors": errors,
        "seconds": round(time.time() - started, 1),
    }

    (dest / "_SOURCE_SNAPSHOT_MANIFEST.json").write_text(
        json.dumps(stats, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    (dest / "_REBUILD_INSTRUCTIONS.md").write_text(
        _rebuild_instructions(), encoding="utf-8")
    return stats


def _rebuild_instructions() -> str:
    restore_lines = "\n".join(
        f"   - copy `<install_root>/{src}`  ->  `{dst}` (under this directory)"
        for dst, src in RESTORE_FROM_INSTALL.items()
    )
    return f"""# How to rebuild Tlamatini from this source snapshot

This directory is a COMPLETE source snapshot generated by `copy_source_assets.py`
(omitting media, secrets, and binaries already present in the installed tree).
`<install_root>` below = the installed Tlamatini directory (where Tlamatini.exe lives).

1. **Restore the omitted binaries** (they were skipped because the install
   already carries them):
{restore_lines}

2. **Restore live secrets** — `Tlamatini/agent/config.json` (and a few agent
   `config.yaml` files) here carry `<KEY goes here>` placeholders. Copy the real
   values from `<install_root>/config.json` (and the installed agents' configs),
   or run `python regen_secrets.py --mode keyed` with a populated `data.keys`.

3. **Install build deps** into a Python 3.12.10 interpreter:
   `python -m pip install -r requirements.txt` (build.py also re-installs them).

4. **Regenerate static files** if needed: `python Tlamatini/manage.py collectstatic
   --noinput` (build.py expects `Tlamatini/staticfiles/`).

5. **Build**: `python build.py --self-modify` (then `python build_uninstaller.py`
   and `python build_installer.py` for the full installer pipeline). The
   `--self-modify` flag re-runs `copy_source_assets.py`, so the NEW build's
   snapshot automatically reflects every modification made here.
"""


def main(argv: List[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[1])
    parser.add_argument("--repo-root", default=str(REPO_ROOT),
                        help="Source repository root (default: this script's directory)")
    parser.add_argument("--dest", default=str(DEFAULT_DEST),
                        help=f"Snapshot destination (default: {DEFAULT_DEST})")
    parser.add_argument("--no-redact", action="store_true",
                        help="Keep secret values verbatim (LOCAL USE ONLY)")
    args = parser.parse_args(argv)

    stats = copy_source_assets(args.repo_root, args.dest, redact=not args.no_redact)
    print(f"Snapshot: {stats['dest']}")
    print(f"  files copied   : {stats['files_copied']}")
    print(f"  files skipped  : {stats['files_skipped']}")
    print(f"  size           : {stats['megabytes_copied']} MB")
    print(f"  files redacted : {stats['files_redacted']}")
    if stats["errors"]:
        print(f"  ERRORS ({len(stats['errors'])}):")
        for e in stats["errors"]:
            print(f"    - {e}")
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
