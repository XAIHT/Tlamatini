"""
copy_source_assets.py — generate the TlamatiniSourceCode self-modify snapshot.

Called by `build.py --self-modify` (and runnable standalone) to assemble the
COMPLETE source-code tree Tlamatini needs to take -> modify -> integrate ->
regenerate her own Tlamatini.exe, while omitting heavy media and everything
already present (or regenerable) in the installed directory tree.

DEEP ASSET ANALYSIS (what a self-rebuild actually needs)
========================================================

The snapshot mirrors the repo-root layout, so a rebuild is simply:
    cd <install_root>/TlamatiniSourceCode && python build.py --self-modify

INCLUDED — everything the build pipeline and the app's source surface touch:
  * Build pipeline (repo root): build.py, build_installer.py,
    build_uninstaller.py, versioning.py, install.py, uninstall.py,
    regen_secrets.py, requirements.txt, pyinstaller_hooks/, eslint.config.mjs,
    package.json, pnpm-lock.yaml, tlamatini_mcp_server.py, this script itself.
  * Self-update capability ("About > Check for updates"): the in-app updater
    agent/self_update.py AND the external file-swap script apply_update.ps1
    (repo root; build.py ships it next to Tlamatini.exe so self_update.py can
    copy it out and run the swap). Both are plain source and ship verbatim.
  * ALL PowerShell helpers (*.ps1): Tlamatini.ps1, CreateShortcut.ps1,
    RemoveShortcut.ps1, register_flw.ps1, unregister_flw.ps1, apply_update.ps1
    (+ CreateShortcut.json).
  * The full Django tree: every .py, .html, .css, .js, .yaml/.yml, .json,
    .pmt (prompt.pmt + monitoring-prompt.pmt), .proto, SKILL.md packages,
    all 76 agent templates, migrations, tests, AuxTests/.
  * Firmware template sources (.ino/.h/.cpp — ArduinoTemplateProject etc.).
  * Docs that drive behavior or onboarding: CLAUDE.md, docs/claude/*,
    agents_descriptions.md (parsed at runtime for tooltips!), README.md,
    BookOfTlamatini.md, ACPX.md, VERSIONING.md, .agents/, .mcps/, .skills/,
    .claude/ (skills + hooks; settings.local.json EXCLUDED — secret fragments),
    .github/, LICENSE, .gitignore, .mcp.json.
  * Small build-REQUIRED binaries: *.ico (build.py --icon + console icon,
    ~39 KB x2), *.wav (notification/hypervisor sounds shipped via
    --add-data static, ~730 KB total), *.svg (spinner). A rebuild FAILS or
    produces a degraded app without these, so they ship despite being binary.

EXCLUDED — heavy media, secrets, and regenerable/duplicated state:
  * Media: .pdf, .pptx, .png, .jpg/.jpeg, .gif, .mp4 ... (~44 MB — the
    agent/images gallery, demo videos, presentation, summary PDF). None are
    inputs to PyInstaller except agent/static/agent/video/XAIHT-Tlamatini.mp4,
    which is listed in the RESTORE manifest (copy back from the installed
    _internal/agent/static tree before rebuilding).
  * jd-cli/jd-cli.jar (3.8 MB) — already at <install_root>/jd-cli verbatim;
    listed in the RESTORE manifest (build.py hard-requires it).
  * Tlamatini/staticfiles/ — regenerated by `manage.py collectstatic`
    (rebuild instruction), never a source of truth.
  * Secrets & local state: data.keys, db.sqlite3, .claude/settings.local.json,
    key.txt/cookie.txt/crumb.json, *.session, *.log, *.pos, _version.py,
    *.version.txt, .build.lock, pkg.zip/*.zip, *.exe/*.dll/*.pyd, *.lnk.
  * Dev/derived trees: .git, __pycache__, node_modules, venv/.venv, build/,
    dist/, agents/pools/, Temp/, Templates/, content_generated/,
    context_files/, applications/, caches — and TlamatiniSourceCode itself
    (recursion guard).
  * SECRET REDACTION: agent/config.json and the agent config.yaml files may
    carry LIVE keys on a dev machine. Values for secret-looking keys are
    replaced with the regen_secrets.py placeholder style "<KEY goes here>";
    at rebuild time Tlamatini restores them from the installed config.json
    (see _REBUILD_INSTRUCTIONS.md written into the snapshot).

The snapshot also gets `_SOURCE_SNAPSHOT_MANIFEST.json` (counts, sizes,
restore list, redacted-file list) and `_REBUILD_INSTRUCTIONS.md` (the exact
restore + rebuild runbook Tlamatini follows).

Usage:
    python copy_source_assets.py                       # -> Tlamatini/agent/TlamatiniSourceCode
    python copy_source_assets.py --dest <dir>          # -> custom destination
    python copy_source_assets.py --no-redact           # keep secrets verbatim (LOCAL ONLY)

From build.py:
    import copy_source_assets
    stats = copy_source_assets.copy_source_assets(repo_root, dest_dir)
"""
